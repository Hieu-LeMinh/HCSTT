    private void jBupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBupdateActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure to update data");
        if(confirm == 0){
            System.out.println(JCBMalaria.getSelectedItem());
            Query_to_database que = new Query_to_database();
            int update_status = que.update_desease_from_doctor(jTseachID.getText(), jTsearchName.getText(), String.valueOf(jCBFever.getSelectedItem()),
                    String.valueOf(JCBMalaria.getSelectedItem()), String.valueOf(JCBtyphoid.getSelectedItem()), 
                    String.valueOf(JCBstomach.getSelectedItem()), String.valueOf(JCBchestProblem.getSelectedItem()));
            
            if( jTDoctorNameSetUp.getText().equalsIgnoreCase("") || jTDoctorIDSetUp.getText().equalsIgnoreCase("") || jTDoctorGenderSetUp.getText().equalsIgnoreCase("") || jTDoctorAgeSetUp.getText().equalsIgnoreCase("")){
                JOptionPane.showMessageDialog(this, "you must enter fully");
            }else{
                int result_store_doctor_infor = que.store_information_doctor(jTseachID.getText(),  jTDoctorNameSetUp.getText()  , jTDoctorIDSetUp.getText(), jTDoctorGenderSetUp.getText(), Integer.parseInt(jTDoctorAgeSetUp.getText()), fileNameImageDoctor);
                if(update_status == 1 && result_store_doctor_infor == 1){
                    JOptionPane.showMessageDialog(this, "this doctor has existed and update successfully");
                }else if(update_status == 1 && result_store_doctor_infor == 2){
                     JOptionPane.showMessageDialog(this, " a new doctor and update successfully");
                }
                else{
                    JOptionPane.showMessageDialog(this, "ID or Name is incorrect");
                }
            }                               
        }
    }//GEN-LAST:event_jBupdateActionPerformed

